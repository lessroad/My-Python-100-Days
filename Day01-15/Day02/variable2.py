a=int(input("a="))
b=int(input("b="))

print('%d+%d=%d'%(a,b,a+b))
print('%d-%d=%d'%(a,b,a-b))
print('%d*%d=%3f'%(a,b,a*b))
print('%d/%d=%3f'%(a,b,a/b))
print('%d // %d = %d' % (a, b, a // b))
print('%d %% %d = %d' % (a, b, a % b))
print('%d ** %d = %d' % (a, b, a ** b))